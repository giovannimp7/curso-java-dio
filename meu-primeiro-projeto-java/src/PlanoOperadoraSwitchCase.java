public class PlanoOperadoraSwitchCase {
    public static void main(String[] args) {
        String plano = "M";

        switch (plano){
            case "T":{
                System.out.println("5GB e Youtube grátis");
            }
            case "M":{
                System.out.println("Whatsapp e Instagram grátis");
            }
            case "B":{
                System.out.println("100 minutos de ligação");
            }
        }

    }
}
